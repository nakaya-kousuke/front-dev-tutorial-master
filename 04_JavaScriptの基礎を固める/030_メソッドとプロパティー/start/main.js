let hello = 'hello would';
console.log(hello);

// length：文字数
let hello2 = 'hello would';
console.log(hello2.length);

// toUpperCase：大文字
let hello3 = 'hello would';
console.log(hello3.toUpperCase());

// substring：文字切り取り
let hello4 = 'hello would';
console.log(hello4.substring(0, 5));

// substring：文字切り取り
let hello5 = 'hello would';
console.log(hello5.slice(0, 5));

// 文字を分割
let hello6 = 'hello would';
console.log(hello6.split(''));

let hello7 = 'hello would';
console.log(hello7.split(' '));